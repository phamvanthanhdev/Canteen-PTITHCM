    private void tblTablesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTablesMouseClicked
        int id = tblTables.getSelectedRow();
        //int id = listTables.get(i).getId();
        if(listTables.get(id).getStatus() == 1){  // Ban nay dang duoc su dung
            btnUpdateOrder.setEnabled(true);
            btnBanHang.setEnabled(false);
            btnChuyenBan.setEnabled(true);
            btnGopBan.setEnabled(true);
        }
        else if(listTables.get(id).getStatus() == 0){   // Ban nay dang trong
            btnBanHang.setEnabled(true);
            btnUpdateOrder.setEnabled(false);
            btnChuyenBan.setEnabled(false);
            btnGopBan.setEnabled(false);
        }
    }//GEN-LAST:event_tblTablesMouseClicked

