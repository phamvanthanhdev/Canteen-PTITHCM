    private void btnBanHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBanHangActionPerformed
        int i = tblTables.getSelectedRow();
        if(listTables.get(i).getStatus() == 0){   // Ban nay dang trong
            //Them don hang moi
            JOptionPane.showMessageDialog(this, "Bàn này đang trống !", "Thông báo lỗi", JOptionPane.DEFAULT_OPTION);
            t.setTable_id(listTables.get(i).getId());

            //Tao 1 don hang moi
            long millis = System.currentTimeMillis();
            Date order_date = new Date(millis);
            int status = 1;  //Trang thai dang hoat dong
            int payments = 0; // Trang thai thanh toan mac dinh
            int type = 0;  // Su dung tai quan mac dinh type = 0 (mang ve 1, online 2)

            //Tao 1 don hang moi
            orderDAO.insertOrderOnTable(username, order_date, status , payments, listTables.get(i).getId(), type, staff_username);

            //Cap nhat trang thai cua ban là dang hoat dong
            tableDAO.updateStatusTable(listTables.get(i).getId(), status);

            new BanHang().setVisible(true);
        }
    }//GEN-LAST:event_btnBanHangActionPerformed

    private void btnMangVeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMangVeActionPerformed
        int b = JOptionPane.showConfirmDialog(this, "Tạo một đơn hàng mang về ?", "Notice", JOptionPane.YES_NO_OPTION);
        if (b == 0) {
            //Tao 1 don hang moi
            long millis = System.currentTimeMillis();
            Date order_date = new Date(millis);
            int status = 1;  //Trang thai dang hoat dong
            int payments = 0; // Trang thai thanh toan mac dinh
            int type = 1;  // Su dung mang ve mac dinh type = 1

            // so ban 0 : khong co ban nao co so ban 0  (chi su dung de ban mang ve)
            t.setTable_id(0);
            //Tao 1 don hang mang ve moi
            orderDAO.insertOrderMangVe(username, order_date, status, payments, type, staff_username);

            new BanHang().setVisible(true);
        }
    }//GEN-LAST:event_btnMangVeActionPerformed

