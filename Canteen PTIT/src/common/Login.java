    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (checkEmpty()) {
            String username = txtUsername.getText().trim();
            String password = String.valueOf(txtPassword.getPassword()).trim();
            if (btnUser.isSelected()) {
                userEntity user = userDAO.selectUserLogin(username, password);
                if (user == null) {
                    JOptionPane.showMessageDialog(this, "Thông tin tài khoản mật khẩu không chính xác!!", "Warring", 2);
                } else {
                    if(!user.isStatus()){
                        JOptionPane.showMessageDialog(this, "Tài khoản đã bị khóa!!", "Warring", 2);
                        return;
                    }
                    new ToanCuc().setUsername(username);
                    
                    JOptionPane.showMessageDialog(this, "Đăng nhập thành công!!", "Warring", 2);
                    this.dispose();
                    new UserDashboard().setVisible(true);
                }
            } else if (btnStaff.isSelected()) {
                NhanVienEntity nv = nvDAO.selectNhanVienDangNhap(username, password, 2);
                if (nv == null) {
                    JOptionPane.showMessageDialog(this, "Thông tin tài khoản mật khẩu không chính xác!!", "Warring", 2);
                } else {
                    if(!nv.isStatus()){
                        JOptionPane.showMessageDialog(this, "Tài khoản đã bị khóa!!", "Warring", 2);
                        return;
                    }
                    new ToanCuc().setUsername(username);
                    
                    JOptionPane.showMessageDialog(this, "Đăng nhập thành công!!", "Warring", 2);
                    this.dispose();
                    new StaffDashboard().setVisible(true);
                }
            }else if (btnAdmin.isSelected()){
                NhanVienEntity nv = nvDAO.selectNhanVienDangNhap(username, password, 1);
                if (nv == null) {
                    JOptionPane.showMessageDialog(this, "Thông tin tài khoản mật khẩu không chính xác!!", "Warring", 2);
                } else {
                    if(!nv.isStatus()){
                        JOptionPane.showMessageDialog(this, "Tài khoản đã bị khóa!!", "Warring", 2);
                        return;
                    }
                    new ToanCuc().setUsername(username);
                    
                    JOptionPane.showMessageDialog(this, "Đăng nhập thành công!!", "Warring", 2);
                    this.dispose();
                    new AdminDashboard().setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

