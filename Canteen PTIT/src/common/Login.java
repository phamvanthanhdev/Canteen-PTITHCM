    // kiêm tra có o nao bo trong khong
    private boolean checkEmpty() {
        if (txtUsername.getText().isEmpty() || String.valueOf(txtPassword.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!!", "Warring", 2);
            return false;
        }
        if (!btnUser.isSelected() && !btnAdmin.isSelected() && !btnStaff.isSelected()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn đối tượng đăng nhập!!", "Warring", 2);
            return false;
        }
        return true;
    }

